class postElements {
    constructor() {
        this.klass = "post";
        this.id = null;
        this.visualsArea = null;
        this.captionsArea = null;
    }
    getId() {
        return this.id
    }
    setId(id) {
        this.id = id;
    }
    getVisualsArea() {
        return this.visualsArea
    }
    setVisualsArea(visualsArea) {
        this.visualsArea = visualsArea;
    }
    getCaptionsArea() {
        return this.captionsArea
    }
    setCaptionsArea(captionsArea) {
        this.captionsArea = captionsArea
    }
};